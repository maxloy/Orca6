using UnityEngine;

public static class Utility
{
	public static Vector3 Midpoint(Vector3 a, Vector3 b)
	{
		return (a + b) / 2f;
	}

	public static Vector4 Midpoint(Vector4 a, Vector4 b)
	{
		return (a + b) / 2f;
	}

	public static Vector2 Midpoint(Vector2 a, Vector2 b)
	{
		return (a + b) / 2f;
	}
	
}
